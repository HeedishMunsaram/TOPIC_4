#list of strings example
city_list = ["Seattle", "New York", "Washington", "Houston", "Miami", "San Diego", "Charlotte", "Atlanta", "Boston", "Baltimore"]
print("The 4th city on the list is ", city_list[3])

#insert example
city_list.insert(2, "Greensboro")
print(city_list)

#remove example
city_list.remove("Atlanta")
print(city_list)

#append example
city_list.append("Chicago")
print(city_list)